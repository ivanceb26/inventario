private void docKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_docKeyTyped
// TODO add your handling code here:
    char caracter = evt.getKeyChar();
    if (((caracter < '0')
            || (caracter > '9'))
            && (caracter != evt.VK_BACK_SPACE)) {
        evt.consume();
    }
}//GEN-LAST:event_docKeyTyped

private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
// TODO add your handling code here:
    if (validarCamposPass()) {
        try {
            usuario = new Usuario();
            usuario.setCedula(doc.getText().toUpperCase());
            usuario.setDireccion(direccion.getText().toUpperCase());
            usuario.setTelefono(tel.getText().toUpperCase());
            usuario.setNombre(nombres.getText().toUpperCase());
            usuario.setApellido(apellidos.getText().toUpperCase());
            usuario.setUser(nomUsuario.getText().toUpperCase());
            usuario.setPassword(enc.md5(paswordField1.getText()));

            if (jRadioAdmin.isSelected()) {
                usuario.setRol("ADMINISTRADOR");
            } else if (jRadioUser.isSelected()) {
                usuario.setRol("USUARIO");
            }


            try {
                control.registrarUsuario(usuario);
                if (control.getUsuario().getRol() == "ADMINISTRADOR") {
                    JOptionPane.showMessageDialog(this, "usuario registrado satisfactoriamente");
                }
                if (label != null) {
                    label.setText(usuario.getNombre() + " " + usuario.getApellido());

                }
                this.dispose();
            } catch (Exception e) {
                Logger.getLogger(UserNewD.class.getName()).log(Level.SEVERE, null, e);
                JOptionPane.showMessageDialog(this, "Usuario ya Registrado ");
                this.dispose();
            }
        } catch (Exception ex) {
            Logger.getLogger(UserNewD.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


}//GEN-LAST:event_jButton2ActionPerformed

