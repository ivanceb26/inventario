    public boolean validarCamposPass() {
        if (!paswordField1.getText().equals("") && !passwordField2.getText().equals("")) {
            if (paswordField1.getText().equals(passwordField2.getText())) {
                return true;
            } else {
                JOptionPane.showMessageDialog(this, "Los campos de contraseña no coinciden");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Los campos de contraseña no pueden estar vacíos");
        }
        return false;
    }

